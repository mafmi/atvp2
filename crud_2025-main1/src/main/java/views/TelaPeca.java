    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        p = new Peca();
        p.setNome(txtNome.getText());
        p.setTipo(txtTipo.getText());
        
        PecaDAO dao = new PecaDAO();
        dao.create(p);
        
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void tblCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCadastradosMouseClicked
        // TODO add your handling code here:
        int linha = tblCadastrados.getSelectRow();
        String id = tblCadastrados.getValueAt(linha,0).toString();
        
        if(linha != -1){
            PecaDAO dao = new PecaDAO();
            p=dao.read(Integer.parseInt(id));
            
            txtNome.setText(p.getNome());
            txtTipo.setText(p.getTipo());
        }
    }//GEN-LAST:event_tblCadastradosMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        p.setNome(txtNome.getText());
        p.setTipo(txtTipo.getText());
        
        PecaDAO dao = new PecaDAO();
        dao.update(p);
        
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if(p != null){
            if(JOptionPane.showConfirmDialog(null, "Tem certeza que deseja EXCLUIR essa Peça?") == JOptionPane.YES_OPTION){
            PecaDAO dao = new PecaDAO();
            dao.destroy(p);

            limpar();
            preencherTabela();
            }
        
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


