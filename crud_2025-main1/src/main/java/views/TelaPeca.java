    public void preencherTabela(){
        DefaultTableModel dtm = (DefaultTableModel) tblCadastrados.getModel();
        dtm.setRowCount(0);
        PecaDAO dao = new PecaDAO(); 
        
        for(Peca p : dao.read()){
            dtm.addRow(new Object[] {
                p.getId(),
                p.getNome(),
                p.getTipo()
            });
        }
    }

